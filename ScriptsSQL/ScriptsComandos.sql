RESOLUÇÃO Nº 002/SEMEC/2020

Dispõe sobre a organização do Ano Escolar e do Ano Letivo nas Unidades de Educacionais da
Rede Municipal de Ensino para o ano 2020 e dá outras providências.

A SECRETÁRIA MUNICIPAL DE EDUCAÇÃO E CULTURAdo município  de Três Lagoas,
Mato Grosso do Sul, no uso de suas atribuições legais conferidas pelo inciso II, do art. 47 da
Lei nº 1.795, de 16 de julho de 2002 e, considerando o disposto na Lei Federal n.º 9394, de 20
de dezembro de 1996, e do decreto nº 052, de 26 de fevereiro de 2019,

Resolve:

Art. 1ºO Ano Escolar de 2020, nas Unidades Educacionais da Rede Municipal de Ensino, terá
a duração de 205 (duzentos e cinco) dias, sendo:

I -200 (duzentos) dias letivos;

II -05 (cinco) dias de Jornada Pedagógica;

III -02 (dois) dias destinados a Exames Finais.

Parágrafo único:O ano Escolar iniciará no dia 05 (cinco) de fevereiro e o ano Letivo no dia 02
(dois) de março de 2020, conforme calendário em anexo.

Art. 2ºCaracteriza-se como dia letivo toda atividade planejada, com data prevista no Calendário
Escolar, com frequência do estudante e efetiva orientação do professor.

Parágrafo único:No Calendário Escolar 2020 da Unidade, deve constar as seguintes datas de
sábados letivos:

  - 09 de maio: refere-se à segunda-feira.

- 11 de julho: refere-se à terça-feira.

- 08 de agosto: refere-se à quarta-feira.

- 03 de outubro: refere-se à quinta-feira.

- 28 de novembro: refere-se à sexta-feira.

Art. 3ºQuando houver absoluta necessidade de interrupção de aulas, independentemente do
motivo, a Unidade de Ensino deverá assegurar o pleno cumprimento da carga horária prevista
no Calendário Escolar.

§  1º  A  não  efetivação  total  ou  parcial  de  dias  letivos,  previstos  no  Calendário  Escolar,
independente do motivo, deverá ter sua reposição assegurada no sábado subsequente.

§ 2º Para o efetivo cumprimento do artigo 3º, a Unidade de Ensino poderá usar os sábados



subsequentes, desde que validado pela Secretaria Municipal de Educação e Cultura (SEMEC).

§ 3º Quando ocorrer de o sábado subsequente ser seguido de feriado letivo ou ser um sábado
de feriado, o cumprimento será no sábado posterior.

§ 4º As reposições, conforme preconizam o artigo 3º, deverão ser cumpridas com a mesma
carga horária e conforme o horário de aula estabelecido para o dia em que houve a interrupção
da aula, respeitando cada turno de funcionamento da Unidade.

§ 5º O descumprimento do disposto no artigo 3º implicará no não cumprimento dos dias letivos
e, consequentemente, prejuízos no desenvolvimento do processo de ensino e aprendizagem
dos estudantes.

Art. 4ºO calendário Escolar da Unidade de Ensino deverá ser elaborado tendo como base o
Calendário Escolar da SEMEC, devidamente aprovado pelo Conselho Municipal de Educação.

§ 1º No calendário Escolar da Unidade deve constar as adequações necessárias, obedecendo
as normativas desta Resolução.

§ 2º O Calendário Escolar deverá ser enviado até a data estabelecida, pela SEMEC, para
aprovação.

§ 3º Qualquer alteração, a ser realizada no Calendário Escolar, já analisado e validado, deverá
ser  comunicada,  formalmente,  à  Secretaria  Municipal  de  Educação  e  Cultura/Diretoria
Educacional e Pedagógica, com antecedência mínima de 05 (cinco) dias úteis, para validação.

Art.  5ºCompete  ao  Núcleo  de  Monitoramento  e  Normas/Inspeção  Escolar  acompanhar  o
cumprimento da carga horária total prevista nas Matrizes Curriculares, assim como a execução
das atividades previstas no Calendário Escolar.

Parágrafo  único:Na  Unidade  de  Ensino  o  responsável  para  o  acompanhamento  e
cumprimento dos dias letivos é a direção da Unidade.

 Art.  6º A  somatória  das aulas  programadas,  incluindo formação continuada e  orientações
pedagógicas, previstas no Calendário Escolar, para serem realizadas durante o ano letivo, ou
seja, durante os 200 (duzentos) dias, não poderá ultrapassar 6% (seis por cento) do total anual
dos dias letivos previstos.

Art. 7ºDo total decorrentes do disposto no artigo anterior, serão 04 (quatro) dias destinados
para uso exclusivo da Secretaria Municipal  de Educação e Cultura e 08 (oito)  dias para a
Unidade de Ensino, sendo distribuídos da seguinte maneira:

04 (quatro) dias destinados para:

- Conselho de classe para serem usados em todas as escolas e, nos Centros de Educação
Infantil, para os Grupos 5 e Grupo 6.

-  Orientações  Pedagógicas  para  serem  usados  nos  Centros  de  Educação  Infantil  que
atendam os Grupos 1, 2, 3 e 4.

04 (quatro) dias destinados para Formação Continuada, correspondentes aos dias não letivos
do mês de outubro:

-  21 de março: refere-se ao dia 13 de outubro.

-  04 de abril: refere-se ao dia 14 de outubro.



-  20 de junho: refere-se ao dia 16 de outubro.

-  12 de setembro: refere-se ao dia 28 de outubro.

04 (quatro) dias destinados para a Secretaria Municipal de Educação e Cultura.

§ 1º O quantitativo de dias registrados no artigo 7º deverão ser previstos dentro do Ano Letivo,
sendo  que  suas  datas  de  efetivação  poderão  ser  alteradas,  em  conformidade  com  a
programação da Secretaria Municipal de Educação e Cultura ou conforme as eventualidades
que poderão surgir no decorrer do Ano Letivo.

§ 2º A ausência parcial ou total do uso do quantitativo desses dias, por parte da Secretaria
Municipal de Educação e Cultura, não implicará na antecipação do término do Ano Letivo.

§ 3º As aulas programadas devem ser planejadas com atividades dirigidas, onde os estudantes
receberão orientações dos professores de como realizar, tendo prazo de entrega, para que o
professor realize as correções e dê a devolutiva aos estudantes, essas aulas programadas não
deverão ser realizadas seguidas de dias em que houver feriados.

§ 4º O tempo destinado às aulas programadas serão usados pela Unidade de Ensino para
garantir  a  Formação  Continuada  aos  professores,  Conselho  de  Classe  e  Orientações
Pedagógicas, visando a melhoria das ações educativas.

Art. 8ºEsta Resolução entra em vigor na data de sua publicação, revogando a publicação da
Resolução nº 001/SEMEC/2020, de 08 de janeiro de 2020.

